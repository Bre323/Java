package programJar;

public class Final {
	public static void main(String[] args) {
		
		// 'final' é usado para que a variável declarada não tenha o seu valor alterado
		// É uma boa prática a declaração de uma variável 'final' em UPPERCASE.
		
		final int X = 0;
		final int Y = 1;
		
		System.out.printf("%d & %d", X, Y);
	}
}
